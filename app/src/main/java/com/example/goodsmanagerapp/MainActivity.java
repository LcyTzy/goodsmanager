    // 加载“过滤后”的货物（搜索关键词 + 分类筛选）
    private void loadFilteredGoods(String keyword, String category) {
        Executors.newSingleThreadExecutor().execute(() -> {
            List<Goods> goodsList;
            if (category.equals("全部") && keyword.isEmpty()) {
                // 无筛选：加载全部
                goodsList = db.goodsDao().getAllGoods();
            } else if (!category.equals("全部") && keyword.isEmpty()) {
                // 仅分类筛选
                goodsList = db.goodsDao().getGoodsByCategory(category);
            } else if (category.equals("全部") && !keyword.isEmpty()) {
                // 仅搜索关键词
                goodsList = db.goodsDao().searchGoods(keyword);
            } else {
                // 分类 + 关键词
                goodsList = db.goodsDao().searchGoodsByCategory(keyword, category);
            }
            handler.post(() -> {
                if (adapter == null) {
                    adapter = new GoodsAdapter(MainActivity.this, goodsList);
                    rvGoods.setAdapter(adapter);
                } else {
                    adapter.setGoodsList(goodsList);
                }
            });
        });
    }

